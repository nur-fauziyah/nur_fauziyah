    private void tampilkan() {
        int row = tabel.getRowCount();
        for (int a=0; a<row; a++) {
            model.removeRow(0);
        }
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_karyawan","root","");
            ResultSet rs = con.createStatement().executeQuery("SELECT * FROM tb_karyawan");
            while(rs.next()) {
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(karyawan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
