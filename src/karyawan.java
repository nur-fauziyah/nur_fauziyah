    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int i = tabel.getSelectedRow();
        
        if(i>-1) {
            selectedId = model.getValueAt(i, 0).toString();
            nama.setText(model.getValueAt(i, 1).toString());
            jabatan.setText(model.getValueAt(i, 2).toString());
            notelp.setText(model.getValueAt(i, 3).toString());
            alamat.setText(model.getValueAt(i, 4).toString());
            status.setSelectedItem(model.getValueAt(i, 5).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        if (selectedId != null) {
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_karyawan", "root", "");
                    String sql = "DELETE FROM tb_karyawan WHERE id_karyawan = '" + selectedId + "'";
                    con.createStatement().executeUpdate(sql);
                    tampilkan();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Gagal menghapus data: " + ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus terlebih dahulu");
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

